<template>
	<div class="seller" ref="seller">
		<div class="seller-content">
			<div class="overview">
				<h1 class='title'>{{seller.name}}</h1>
				<div class="desc border-1px">
					<star class="star" :size="36" :score="seller.score"></star>
					<span class="text">（{{seller.ratingCount}}）</span>
					<span class="text">月售{{seller.sellCount}}单</span>
				</div>
				<ul class="remark">
					<li class="block">
						<h2>起送价</h2>
						<div class="content">
							<span class="stress">{{seller.minPrice}}</span>元
						</div>
					</li>
					<li class="block">
						<h2>商家配送</h2>
						<div class="content">
							<span class="stress">{{seller.deliveryPrice}}</span>元
						</div>
					</li>
					<li class="block">
						<h2>平均配送时间</h2>
						<div class='content'>
							<span class="stress">{{seller.deliveryTime}}</span>分钟
						</div>
					</li>
				</ul>
				<div class="favorite" @click="toggleFavorite($event)">
					<span class="icon-favorite" :class="{'active':favorite}"></span>
					<span class="text">{{favorateText}}</span>
				</div>
			</div>
			<split></split>
			<div class="bulletin">
				<h1 class="title">公告与活动</h1>
				<div class="content-wrapper border-1px">
					<p class="content">{{seller.bulletin}}</p>
				</div>
				<div v-if="seller.supports" class="supports">
					<div class="support-item border-1px" v-for="(item,index) in seller.supports">
						<span class="icon" :class="classMap[seller.supports[index].type]"></span>
						<span class="text">{{seller.supports[index].description}}</span>
					</div>
				</div>
			</div>
			<split></split>
			<div class="pics">
				<title>商家实景</title>
				<div class="pic-wrapper" ref="picWrapper">
					<ul class="pic-list" ref="picList">
						<li class="pic-item" v-for="pic in seller.pics">
							<img :src="pic" width="120" height="90"/>
						</li>
					</ul>
				</div>
			</div>
			<split></split>
			<div class="info">
				<h1 class="title border-1px">商家信息</h1>
				<ul>
					<li class="info-item border-1px" v-for="item in seller.infos">
						{{item}}
					</li>
				</ul>
			</div>
		</div>
	</div>
</template>
<script>
import {saveToLocal,loadFromLocal} from "../../common/js/store.js"
import BScroll from "better-scroll"
import star from "../star/star.vue"
import split from "../split/split.vue"
 export default {
 	props:{
 		seller:{
 			type:Object
 		}
 	},
 	computed:{
 		favorateText(){
 			return this.favorite?"已收藏":"收藏"
 		}
 	},
 	data(){
 		return {
 			//从缓存中去读取
 			favorite:(()=>{
 				return loadFromLocal(this.seller.id,"favorite",false);
 			})()
 		}
 	},
 	created(){
		this.classMap=["decrease","discount","special","guarantee","invoice"];
	},
	//seller页刷新会执行watch,从其他页面切换到seller页先执行mounted
	watch:{
		"seller"(){
			this.$nextTick(()=>{
				this._initScroll();
				this._initPics();
			})
		}
	},
	//dom渲染完毕
	mounted(){
		this.$nextTick(()=>{
			this._initScroll();
			this._initPics();
			//dom被挂载的时候seller还是个空对像，所以使用watch监听seller的变化，或者使用update()钩子函数，但是最好使用watch和计算属性
		});
	},
	methods:{
		toggleFavorite(event){
			if(!event._constructed){
				return;
			}
			this.favorite=!this.favorite;
			saveToLocal(this.seller.id, "favorite", this.favorite);
		},
		_initScroll(){
			if(!this.scroll){
				this.scroll=new BScroll(this.$refs.seller,{
					click:true
				})
			}else{
				this.scroll.refresh();
			}
		},
		_initPics(){
			//有图片的时候设置ul的宽度，因为better-scroll是内容超过包含块儿的时候才可滚动
			if(this.seller.pics){
			let picWidth=120;
			let margin=16;
			let width=(picWidth+margin)*this.seller.pics.length-margin;
			//设置宽度
			this.$refs.picList.style.width=width+"px";
			this.$nextTick(()=>{
				if(this.scroll){
					this.scroll=new BScroll(this.$refs.picWrapper,{
					scrollX:true,
					eventPassthrough:'vertical'  //忽略竖向滚动
					});
				}else{
					this.scroll.refresh();
				}
			})
		}
		}
	},
 	components:{
 		star,
 		split
 	}
 }
</script>
